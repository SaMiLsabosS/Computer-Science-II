
public class Conversion
{
    public static void main ( String[] args )
    {
        //decimal conversions
        double doubleOne = 2.54;
        double doubleTwo = 0.9144;
        double doubleThree = 1.609;
        double doubleFour = 0.3937;
        double doubleFive = 1.094;
        double doubleSix = 0.6214;
        double doubleSeven = 9.464E12;
        System.out.println("       Units and Conversion Factors\n");
        System.out.println("----------------------------------------------\n");
        System.out.println("1 inch = "+doubleOne+" centimeters\n");
        System.out.println("1 yard = "+doubleTwo+" meters\n");
        System.out.println("1 mile = "+doubleThree+" kilometers\n");
        System.out.println("1 centimeter = "+doubleFour+" inches\n");
        System.out.println("1 meter = "+doubleFive+" yards\n");
        System.out.println("1 kilometer = "+doubleSix+" miles\n");
        System.out.println("1 light year = "+doubleSeven+" kilometers\n");
    }
}
